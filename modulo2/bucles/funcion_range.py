#funcion range()
#se usa comunmente con for para repetir un bloque un numero especifico de veces
#sintaxis:
# range(inicio, fin, paso)
#ejemplo:

for i in range(5):  # de 0 a 4
    print(i)

for i in range(1, 6):  # de 1 a 5
    print(i)

for i in range(10, 0, -2):  # cuenta regresiva de 10 a 2 en pasos de -2
    print(i)